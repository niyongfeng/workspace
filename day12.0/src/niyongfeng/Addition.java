package niyongfeng;

public class Addition {
	private int m;
	private long n;
	private float o;
	private double p;
	private String q;
	
	public Addition(int m1, int m2) {
		this.m = m1 + m2;
		System.out.println(m);
	}
	
	public Addition(long n1, long n2) {
		this.n = n1 + n2;
		System.out.println(n);
	}
	
	public Addition(float o1, float o2) {
		this.o = o1 + o2;
		System.out.println(o);
	}
	
	public Addition(double p1, double p2) {
		this.p = p1 + p2;
		System.out.println(p);
	}
	
	public Addition(String q1, String q2) {
		this.q = q1 + q2;
		System.out.println(q);
	}

	
	
	
	
}
